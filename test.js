function getZipCode(id){
  return new Promise(async(resolve, reject) => {
    console.log('getZipCode ' + id);
    let node = document.getElementById('shipping_country');
    var ev = new Event('change');
    node.value = id;
    node.dispatchEvent(ev);
    await sleep(1000)
    if( $('input.wt-input.wt-grid__item-md-4').length == 1){
      resolve(true)
    }else{
      resolve(false)
    }
  });
}

function sleep(time){
  return new Promise((resolve, reject) => {
    setTimeout(()=>{
      resolve()
    }, time )
  });
}
